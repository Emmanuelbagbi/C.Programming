    printf("cos(%lf) = %lf\n", 0.5, cos(0.5));         // Cosine function
    printf("tan(%lf) = %lf\n", 0.5, tan(0.5));         // Tangent function
    
    
    printf("exp(%lf) = %lf\n", 1.0, exp(1.0));         // Exponential function
